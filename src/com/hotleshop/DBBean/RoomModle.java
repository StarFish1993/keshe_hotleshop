package com.hotleshop.DBBean;

public class RoomModle {
	private int RMID;
	private String name;
	private String prciec;
	public int getRMID() {
		return RMID;
	}
	public void setRMID(int rMID) {
		RMID = rMID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPrciec() {
		return prciec;
	}
	public void setPrciec(String prciec) {
		this.prciec = prciec;
	}
}
